length = 5      #длинна прямоугольника
breadth = 2     #ширина прямоугольника

area = length * breadth     #присваивание переменной area результата умножения переменных length и breadth
print('Площадь равна', area)    #вывод в одну строку
print('Периметр равен', 2 * (length + breadth)) #вывод резельтатов вычисление без его записи в переменную
